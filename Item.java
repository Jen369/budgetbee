public class Item{
    public class Item{
        protected int amount;
        protected String name;
    
        public void set_amount(int val){
            this.amount = val;
        }
        public void get_amount(){
            return amount;
        }
        public void set_name(String title){
            this.name = title;
        }
        public void get_name(){
            return name;
        }
    }
}
